unsigned char* generate_sha256(const char *input){
    unsigned char* output = (unsigned char *)malloc(SHA256_DIGEST_LENGTH * sizeof(char));
    SHA256_CTX sha256;
    SHA256_Init(&sha256);
    SHA256_Update(&sha256, input, strlen(input));
    SHA256_Final(output, &sha256);
    return output;
}
